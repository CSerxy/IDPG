@register_model_architecture('test_memory', 'test_memory')
def base_architecture(args):
    args.encoder_layers = getattr(args, 'encoder_layers', 12)
    args.encoder_embed_dim = getattr(args, 'encoder_embed_dim', 768)
    args.encoder_ffn_embed_dim = getattr(args, 'encoder_ffn_embed_dim', 3072)
    args.encoder_attention_heads = getattr(args, 'encoder_attention_heads', 12)

    args.activation_fn = getattr(args, 'activation_fn', 'gelu')
    args.last_activation_fn = getattr(args, 'last_activation_fn', 'relu')
    args.pooler_activation_fn = getattr(args, 'pooler_activation_fn', 'tanh')

    args.dropout = getattr(args, 'dropout', 0.1)
    args.attention_dropout = getattr(args, 'attention_dropout', 0.1)
    args.activation_dropout = getattr(args, 'activation_dropout', 0.0)
    args.pooler_dropout = getattr(args, 'pooler_dropout', 0.0)
    args.encoder_layers_to_keep = getattr(args, 'encoder_layers_to_keep', None)
    args.encoder_layerdrop = getattr(args, 'encoder_layerdrop', 0.0)

    args.act_dropout = getattr(args, 'act_dropout', 0.0)

    args.no_token_positional_embeddings = getattr(args, 'no_token_positional_embeddings', False)
    args.num_segment = getattr(args, 'num_segment', 2)

    args.sentence_out_dim = getattr(args, 'sentence_out_dim', 1024)
    args.sentence_class_num = getattr(args, 'sentence_class_num', 2)

    args.apply_bert_init = getattr(args, 'apply_bert_init', False)

    args.encoder_normalize_before = getattr(args, 'encoder_normalize_before', False)
    args.classification_head_name = getattr(args, 'classification_head_name', 'imdb_head')
    args.sentence_classification_head = getattr(args, 'sentence_classification_head', 0)



@register_model_architecture('test_memory', 'tm_base')
def bert_base_architecture(args):
    base_architecture(args)

